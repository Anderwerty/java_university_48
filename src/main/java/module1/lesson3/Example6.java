package module1.lesson3;

public class Example6 {
    public static void main(String[] args) {
        float a = 10.0F;
        float b = 3;

        System.out.println(a+"+"+b+"="+(a+b));
        System.out.println(a+"-"+b+"="+(a-b));
        System.out.println(a+"*"+b+"="+(a*b));
        System.out.println(b+"/"+a+"="+(b/a));
        System.out.println(a+"/"+b+"="+(a/b));
    }
}
