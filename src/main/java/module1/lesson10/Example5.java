public class Example5 {
    public static void main(String[] args) {
        System.out.print("hello\n");
        System.out.println();

        System.out.println("\thello");
        System.out.println();
        System.out.println("hello\bhello");
        System.out.println();
        System.out.println("12345\rhello");
        System.out.println("\\");
        System.out.println("'");
        System.out.println("\'");

        System.out.println("\"");

        System.out.println("123456\f789");
        System.out.println();
    }
}
